// String
const myVariable = "Mathematics";

// Length of string
console.log(myVariable.length); // length of the variable
console.log("Every good boy does fine".length); // length of the complete sentence

// charAt method
console.log(myVariable.charAt(4)); // e

// indexOf method
console.log(myVariable.lastIndexOf(""));  

// slice method
console.log(myVariable.slice(5, 8));
console.log(myVariable.slice(5));
console.log(myVariable.slice(5, 6));
console.log(myVariable.slice(5, 5));

// toUpperCase method
console.log(myVariable.toUpperCase());

// toLowerCase method
console.log(myVariable.toLowerCase());

// includes method // true or false
console.log(myVariable.includes("cd"));

// split method // ouput in array
console.log(myVariable.split("a"));
console.log(myVariable.split(""));
console.log("Tanay, Vaswani, Loves, DevOps".split(","));
console.log("Tanay Vaswani is placed at $450K in San-Francisco right after college!".split(" "));






